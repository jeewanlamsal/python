#You are given a string and your task is to swap cases. In other words, convert all lowercase letters
#to uppercase letters and vice versa.
def swap_case(input_string):
  
    return input_string.swapcase()

# Example usage
input1 = "Hello World"
output1 = swap_case(input1)
print(f"Input: {input1}\nOutput: {output1}")

input2 = "PyThOn Is FuN"
output2 = swap_case(input2)
print(f"Input: {input2}\nOutput: {output2}")

